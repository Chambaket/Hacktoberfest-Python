# Question is: Number of Minutes in seven weeks

total_hours = 24
total_mins = 60
total_mins_per_day = total_hours * total_mins
print("Total minutes per day: ",total_mins_per_day)

one_week = 7 #for one week seven days
seven_weeks = one_week * 7 #seven weeks * one week
print("Seven weeks: ",seven_weeks)


total_mins_one_week = total_mins_per_day * one_week
total_mins_seven_weeks = total_mins_per_day * seven_weeks
print("Number of Minutes for one week is",total_mins_one_week)
print("Number of Minutes for seven weeks is",total_mins_seven_weeks)



